package com.vortex.cloud.ums.dto.base;

import java.util.Date;

import com.vortex.cloud.vfs.data.model.SerializableObject;

public class BackDeleteModelDto implements SerializableObject {
	private static final long serialVersionUID = 1L;
	/** 备份删除 0:未删除，1：已删除 **/
	private Integer beenDeleted;
	/** 删除时间 **/
	private Date deletedTime;
	/** 主键 uuid **/
	private String id;
	/** 状态 **/
	private Integer status;
	/** 记录创建时间 **/
	private Date createTime;
	/** 记录最后修改时间 **/
	private Date lastChangeTime;

	public Integer getBeenDeleted() {
		return beenDeleted;
	}

	public void setBeenDeleted(Integer beenDeleted) {
		this.beenDeleted = beenDeleted;
	}

	public Date getDeletedTime() {
		return deletedTime;
	}

	public void setDeletedTime(Date deletedTime) {
		this.deletedTime = deletedTime;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getLastChangeTime() {
		return lastChangeTime;
	}

	public void setLastChangeTime(Date lastChangeTime) {
		this.lastChangeTime = lastChangeTime;
	}
}
